
# Arithmetic Operators

# Addition:              5 + 9
# Substraction:          5 - 8
# Multiplication:        5 * 7
# Division:              5 / 6
# Floor Division:        5 // 9
# Exponent:              5 ** 3
# Modulus:               5 % 3

print(5 + 9)
